
# запитуємо висоту та сторону
side_of_triangle = float(input("Введіть сторону трикутника: "))
height_of_triangle = float(input("Введіть висоту трикутника: "))

# Визначаємо площу
area = 0.5 * side_of_triangle * height_of_triangle

# Виводимо значення
print(f"Площа даного трикутника: {area}")