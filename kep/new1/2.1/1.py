import math

print("Введіть координати вершин трикутника:")
x1, y1 = map(float, input("Введіть x1 та y1 (через пробіл): ").split())
x2, y2 = map(float, input("Введіть x2 та y2 (через пробіл): ").split())
x3, y3 = map(float, input("Введіть x3 та y3 (через пробіл): ").split())

# Обчислення довжин сторін
a = math.sqrt((x1 - x2)**2 + (y1 - y2)**2)
b = math.sqrt((x2 - x3)**2 + (y2 - y3)**2)
c = math.sqrt((x3 - x1)**2 + (y3 - y1)**2)

# Обчислення напівпериметру
p = (a + b + c) / 2

# Обчислення площі за формулою Герона
S = math.sqrt(p * (p - a) * (p - b) * (p - c))
print(f"Площа трикутника: {S:.2f}")

