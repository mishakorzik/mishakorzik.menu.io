import math

# Зчитування координат векторів
a1 = float(input("Введіть першу координату вектора a (a1): "))
a2 = float(input("Введіть другу координату вектора a (a2): "))
b1 = float(input("Введіть першу координату вектора b (b1): "))
b2 = float(input("Введіть другу координату вектора b (b2): "))

# Обчислення скалярного добутку векторів
scalar_product = a1 * b1 + a2 * b2

# Обчислення модулів векторів
mod_a = math.sqrt(a1**2 + a2**2)
mod_b = math.sqrt(b1**2 + b2**2)

# Перевірка на ненульову довжину векторів
if mod_a == 0 or mod_b == 0:
    print("Один з векторів має нульову довжину. Обчислення неможливе.")
else:
    # Обчислення косинуса кута між векторами
    cos_angle = scalar_product / (mod_a * mod_b)

    print(f"Скалярний добуток векторів = {scalar_product:.2f}")
    print(f"Косинус кута між векторами = {cos_angle:.2f}")
    print("")
    print(f"Модуль вектора a = {mod_a:.2f}")
    print(f"Модуль вектора b = {mod_b:.2f}")
