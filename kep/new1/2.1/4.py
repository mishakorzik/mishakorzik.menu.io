import math

# Зчитування довжин сторін трикутника
a = float(input("Введіть сторону a: "))
b = float(input("Введіть сторону b: "))
c = float(input("Введіть сторону c: "))

# Обчислення напівпериметру
p = (a + b + c) / 2

# Обчислення площі за формулою Герона
S = math.sqrt(p * (p - a) * (p - b) * (p - c))

print(f"Площа трикутника = {S:.2f}")
