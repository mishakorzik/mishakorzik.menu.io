import math

# Зчитування значень ємності і індуктивності
C = float(input("Введіть ємність конденсатора (C) в фарадах: "))
L = float(input("Введіть індуктивність котушки (L) в генрі: "))

# Обчислення періоду коливань
T = 2 * math.pi * math.sqrt(L * C)

# Обчислення частоти коливань
v = 1 / T if T != 0 else 0

print(f"Період коливань T = {T:.2f} секунд")
print(f"Частота коливань v = {v:.3f} Гц")
