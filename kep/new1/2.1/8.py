import math

# Зчитування коефіцієнтів
a = float(input("Введіть коефіцієнт a: "))
b = float(input("Введіть коефіцієнт b: "))
c = float(input("Введіть коефіцієнт c: "))

# Перевірка, що a не дорівнює нулю
if a == 0:
    print("Коефіцієнт a не може дорівнювати нулю.")
else:
    # Обчислення дискримінанта
    D = b**2 - 4 * a * c

    # Перевірка на дійсність коренів
    if D < 0:
        print("Корені рівняння є комплексними.")
    else:
        # Обчислення коренів рівняння
        x1 = (-b + math.sqrt(D)) / (2 * a)
        x2 = (-b - math.sqrt(D)) / (2 * a)

        print("Корені рівняння:")
        print(f"x1 = {x1:.2f}")
        print(f"x2 = {x2:.2f}")
