import math

n = 1000
lam = 0.03
P1 = 7e-4
tau = 3.1e-7

# Введення значення 'c'
c = float(input("Введіть значення швидкості 'c': "))

# Обчислення енергії одного імпульсу
W1 = P1 * tau

# Обчислення кількості довжин хвиль в одному імпульсі
k = (c * tau) / lam

# Обчислення середньої корисної потужності станції
P = W1 * k

print(f"Енергія одного імпульсу W₁ = {W1:.2f} Дж")
print(f"Кількість довжин хвиль в одному імпульсі k = {k:.2f}")
print(f"Середня корисна потужність станції P = {P:.2f} Вт")
