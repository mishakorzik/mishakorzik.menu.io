import math

# Зчитування радіусів трьох куль
R1 = float(input("Введіть радіус першої кулі: "))
R2 = float(input("Введіть радіус другої кулі: "))
R3 = float(input("Введіть радіус третьої кулі: "))

# Обчислення об'ємів куль
v1 = (4/3) * math.pi * R1**3
v2 = (4/3) * math.pi * R2**3
v3 = (4/3) * math.pi * R3**3

# Обчислення середнього об'єму
v_avg = (v1 + v2 + v3) / 3

# Виведення результатів
print(f"Об'єм першої кулі = {v1:.2f}")
print(f"Об'єм другої кулі = {v2:.2f}")
print(f"Об'єм третьої кулі = {v3:.2f}")
print(f"Середній об'єм = {v_avg:.2f}")
