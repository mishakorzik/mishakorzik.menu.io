import math

# Зчитування моменту часу
t = float(input("Введіть момент часу: "))

# Обчислення відстані
S = t**3 - math.sqrt(t)

# Обчислення швидкості
v = 3*t**2 - 1/(2*math.sqrt(t))

# Виведення результатів
print(f"Відстань у момент часу t = {S:.2f}")
print(f"Швидкість у момент часу t = {v:.2f}")
