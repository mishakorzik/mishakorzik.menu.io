import math

# Введення значення 'x'
x = float(input("Введіть значення 'x': "))

if x <= 0:
    print("Помилка: 'x' має бути більше 0.")
elif 2 * x - 1 == 0 or x**2 + 3 * x == 0:
    print("Помилка: Ділення на нуль.")
else:
    # Основні обчислення
    term1 = (x - math.log(x)) / (2 * x - 1)
    term2 = (2 * x - 1) / (x**2 + 3 * x)

    y = term1 + term2
    print(f"Значення другого виразу = {y:.2f}")
