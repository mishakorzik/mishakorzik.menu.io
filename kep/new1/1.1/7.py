import math

# Обчислення виразу
x = float(input("Введіть значення 'x': "))
s = float(input("Введіть значення 's': "))

# Перевірка знаменників
if 2 * x + 1 == 0:
    print("Помилка: Ділення на нуль, оскільки 2*x + 1 = 0.")
elif 1 + 3 * x == 0:
    print("Помилка: Ділення на нуль, оскільки 1 + 3*x = 0.")
else:
    # Основні обчислення
    term1 = (math.sin(x) - x**2) / (2 * x + 1)
    term2 = ((1 + x)**s) / (1 + 3 * x)

    y = term1 + term2
    print(f"Значення виразу = {y:.2f}")
