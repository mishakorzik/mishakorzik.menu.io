import math

# Обчислення виразу
x = float(input("Введіть значення 'x' для виразу: "))

# Перевірка знаменників
if x <= 0:
    print("Помилка: 'x' має бути додатнім для визначення.")
elif x**2 + 1 == 0:
    print("Помилка: Ділення на нуль, оскільки x**2 + 1 = 0.")
elif 2 * x**2 + 1 == 0:
    print("Помилка: Ділення на нуль, оскільки 2*x**2 + 1 = 0.")
else:
    # Основні обчислення
    term1 = (math.log(x) + 2 * x) / (x**2 + 1)
    term2 = (x + 1) / (2 * x**2 + 1)

    y = term1 + term2
    print(f"Значення виразу = {y:.2f}")
