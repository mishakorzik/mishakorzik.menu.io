# Імпортуємо бібліотеку для роботи з математичними функціями
import math

# Запитуємо радіус клумби
radius = float(input("Введіть радіус клумби: "))

# Дізнаємось периметр та площу за даним радіусом
perimeter = 2 * math.pi * radius
area = math.pi * pow(radius, 2)

#Виводимо значення
print(f"Периметр дорівнює: {perimeter}")
print(f"Площа дорівнює: {area}")