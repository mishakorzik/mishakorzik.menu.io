# Введення вартості (копійок за хвилину) для кожного населеного пункту
c1 = float(input("Введіть вартість хвилини для 1-го пункту (коп): "))
c2 = float(input("Введіть вартість хвилини для 2-го пункту (коп): "))
c3 = float(input("Введіть вартість хвилини для 3-го пункту (коп): "))

# Введення тривалості кожної розмови
t1 = float(input("Введіть тривалість розмови для 1-го пункту (хв): "))
t2 = float(input("Введіть тривалість розмови для 2-го пункту (хв): "))
t3 = float(input("Введіть тривалість розмови для 3-го пункту (хв): "))

# Обчислення вартості кожної розмови
cost1 = c1 * t1
cost2 = c2 * t2
cost3 = c3 * t3

# Загальна сума до оплати
total_cost = cost1 + cost2 + cost3

# Вивід результатів
print(f"Вартість розмови з 1-м пунктом: {cost1:.2f} коп")
print(f"Вартість розмови з 2-м пунктом: {cost2:.2f} коп")
print(f"Вартість розмови з 3-м пунктом: {cost3:.2f} коп")
print(f"Загальна сума до оплати: {total_cost:.2f} коп")
