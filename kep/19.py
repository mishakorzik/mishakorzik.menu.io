# Введення швидкостей
v1 = float(input("Введіть швидкість 1 (км/год): "))
v2 = float(input("Введіть швидкість 2 (км/год): "))
v3 = float(input("Введіть швидкість 3 (км/год): "))

# Введення часу руху
t1 = float(input("Введіть час руху з швидкістю v1 (год): "))
t2 = float(input("Введіть час руху з швидкістю v2 (год): "))
t3 = float(input("Введіть час руху з швидкістю v3 (год): "))

# Обчислення пройдених шляхів
S1 = v1 * t1
S2 = v2 * t2
S3 = v3 * t3

# Загальний пройдений шлях
S = S1 + S2 + S3

# Вивід результатів
print(f"Шлях з швидкістю {v1} км/год: {S1:.2f} км")
print(f"Шлях з швидкістю {v2} км/год: {S2:.2f} км")
print(f"Шлях з швидкістю {v3} км/год: {S3:.2f} км")
print(f"Загальний шлях: {S:.2f} км")
