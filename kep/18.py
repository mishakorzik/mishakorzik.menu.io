import math

# Введення радіуса, висоти та твірної конуса
r = float(input("Введіть радіус основи конуса: "))
h = float(input("Введіть висоту конуса: "))
l = float(input("Введіть твірну конуса: "))

# Обчислення площі бічної поверхні та об'єму
S = math.pi * r * l
V = (math.pi * r**2 * h) / 3

# Вивід результатів
print(f"Площа бічної поверхні конуса: {S:.2f}")
print(f"Об'єм конуса: {V:.2f}")
