import math

# Введення площі круга
S = float(input("Введіть площу круга: "))

# Обчислення радіуса
r = math.sqrt(S / math.pi)

# Обчислення сторони квадрата
a = 2 * r

# Обчислення площі та периметра квадрата
P = a ** 2
L = 4 * a

# Відповідь
print(f"Сторона квадрата: {a:.2f}")
print(f"Площа квадрата: {P:.2f}")
print(f"Периметр квадрата: {L:.2f}")
