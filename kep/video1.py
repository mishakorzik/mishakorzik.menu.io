# запитуємо у користувача трицифрове число
number = int(input("Введіть трицифрове число: "))

# Отримуємо цифри числа
a = number // 100
b = (number % 100) // 10
c = number % 10

# Формуємо нове число, розміщуючи цифри у зворотньому порядку
reversed_number = c * 100 + b * 10 + a
print(f"Число у зворотньому порядку: {reversed_number}")
