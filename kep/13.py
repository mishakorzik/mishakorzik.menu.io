# Введення продуктивності кожної труби
pipe1 = float(input("Введіть продуктивність першої труби (м³/год): "))
pipe2 = float(input("Введіть продуктивність другої труби (м³/год): "))
pipe3 = float(input("Введіть продуктивність третьої труби (м³/год): "))

# Введення часу роботи труб
time = float(input("Скільки годин працювали труби? "))

# Обчислення загального об'єму води
total_volume = (pipe1 + pipe2 + pipe3) * time

# Відповідь
print(f"Загальний об'єм: {total_volume}")
